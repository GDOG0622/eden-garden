export function buildUserPrompt({
    name, race, age, height, weight, measurements,
    cycleLength, menstrualDuration, symptoms, persona,
    currentState, chatHistory,
    knowledgeModules = [],
}) {
    // 将命中的知识模块拼成一个段落插入提示词
    const knowledgeSection = knowledgeModules.filter(m => m && m.trim()).length
        ? '\n\n## 当前情景适用参考知识（请结合以下知识更新状态）\n\n' +
          knowledgeModules.filter(m => m && m.trim()).join('\n\n---\n\n')
        : '';

    const personaLine = persona ? `\n人设摘要：${persona}` : '';

    return `角色基本信息：
名字：${name}
种族：${race}
年龄：${age}岁
身高：${height}
体重：${weight}
三围：${measurements}
月经周期（C）：${cycleLength}天（即多久来一次月经）
经期持续（M）：${menstrualDuration}天
月经症状：${symptoms}${personaLine}

当前状态（上次记录）：
${currentState}

最新剧情：
${chatHistory}${knowledgeSection}

请输出更新后的状态，格式如下（字段顺序和名称不可变动）：
<伊甸园>
      （从剧情推断故事内时间，格式 YYYY-MM-DD HH:MM；无法确定则保持原值）
      阶段|（月经/卵泡/排卵/黄体 或 孕X期 等，写明第几天/周）
      活动|（此刻在做什么 → 对身体有何影响；未出现在对话中则大胆推断，禁止敷衍）
      种族|
      年龄|
      身高|
      体重|
      三围|
      乳房|（解剖学描述形状及当前反应，体现活动影响）
      小穴|（根据生理状态和当前活动描述）
      子宫|（宫压+生理状态+当前受孕概率；孕期描述羊膜/羊水/胎儿；非孕时明确写孕率高低）
      后庭|（15字内；正常则写"正常"）
      特质|（身体非典型特征；孕期写妊娠症状；可体现活动引发的临时特征）
      精子|（宫内总量/来源/活性；无则写"无"）
      卵子|（宫内卵子数；无则写"无"）
      胎数|（孕：胎数和性别；非孕：已产胎数，无则写"0"）
      父亲|（孕：生父名；非孕：N/A）
      健康|（孕：胎儿健康/孕检提醒；非孕：N/A）
</伊甸园>`;
}

